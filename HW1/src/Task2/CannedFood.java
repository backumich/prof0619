package Task2;

public class CannedFood {
    private String name;
    private int addWater;
    private int addSugar;

    public String getName() {
        return name;
    }

    public int getAddWater() {
        return addWater;
    }

    public int getAddSugar() {
        return addSugar;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setAddWater(int addWater) {
        this.addWater = addWater;
    }

    public void setAddSugar(int addSugar) {
        this.addSugar = addSugar;
    }
}